'use strict';
//=============================================================================
/**
* dependencies
*/
//=============================================================================
const mongoose = require('mongoose');
//=============================================================================
/**
*contract schema
*/
//=============================================================================
const ContractSchema = mongoose.Schema({
  address: {
    type: String,
    required: true
  }
  });
//=============================================================================
/**
*Create contract model
*/
//=============================================================================
const ContractModel = mongoose.model('Contract', ContractSchema);
//==============================================================================
/**
*Export contract model
*/
//=============================================================================
module.exports = ContractModel;
//=============================================================================
